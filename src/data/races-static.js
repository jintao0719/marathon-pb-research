// 导出获取赛事的函数
export function getAllRaces() {
  return racesData.races;
}

export function getRaceById(id) {
  return racesData.races.find(race => race.id === parseInt(id));
}

export function searchRaces(query) {
  const lowerQuery = query.toLowerCase();
  return racesData.races.filter(race => 
    race.name.toLowerCase().includes(lowerQuery) ||
    race.location.toLowerCase().includes(lowerQuery) ||
    race.region.toLowerCase().includes(lowerQuery)
  );
}

export function getRacesByStatus(status) {
  if (!status || status === 'all') return racesData.races;
  return racesData.races.filter(race => race.status === status);
}

export function getRacesByRegion(region) {
  if (!region || region === 'all') return racesData.races;
  return racesData.races.filter(race => 
    race.region.includes(region) || 
    race.province.includes(region)
  );
}

export function getOpenRaces() {
  return racesData.races.filter(race => race.status === 'open');
}

export function getUpcomingRaces() {
  return racesData.races.filter(race => race.status === 'upcoming');
}

export function getFinishedRaces() {
  return racesData.races.filter(race => race.status === 'finished');
}
